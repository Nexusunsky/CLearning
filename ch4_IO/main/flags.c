#include <stdio.h>

int main(void)
{
    /*按照十六进制数输出*/
    printf("%x %X %#x\n", 31, 31, 31);

    /*使用%和d之间的空格输出在正值前面生成前导空格，负值前面不产生前导空格而是符号*/
    printf("**%d**% d**% d**\n", 42, 42, -42);

    /*%5.3d表明 生成足够的前导0 以满足最小位数的要求*/
    printf("**%5d**%5.3d**%05d**%05.3d**\n", 6, 6, 6, 6);

    return 0;
}