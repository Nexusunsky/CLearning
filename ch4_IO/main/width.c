#include <stdio.h>
#define PAGES 959

int main(void)
{

    /*不带任何参数，对应的输出结果与带整数字段宽度的转换说明的输出结果相同*/
    printf("*%d*\n", PAGES);

    /*对应的输出结果是2字段宽度，因为待打印的整数有三位数字，所有字段宽度自动扩大以符合整数的长度*/
    printf("*%2d*\n", PAGES);

    /*对应的输出结果有10个空格宽度，实际上在两个星号之间有7个空格和三位数字，并且数字位于字段右侧*/
    printf("*%10d*\n", PAGES);

    /*-标记说明打印的数字位于字段的左侧*/
    printf("*%-10d*\n", PAGES);

    return 0;
}
